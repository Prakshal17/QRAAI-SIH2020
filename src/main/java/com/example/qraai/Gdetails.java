package com.example.qraai;

public class Gdetails {

    Integer sn=0,qi=0,qrc=0,price=0;
    String date="",name="";
    public Gdetails() {
    }

    public Integer getSn() {
        return sn;
    }

    public void setSn(Integer sn) {
        this.sn = sn;
    }

    public Integer getQi() {
        return qi;
    }

    public void setQi(Integer qi) {
        this.qi = qi;
    }

    public Integer getQrc() {
        return qrc;
    }

    public void setQrc(Integer qrc) {
        this.qrc = qrc;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
